    public void ordenar() throws Exception {
        String atributo = cbxAtri.getSelectedItem().toString();
        TipoOrdenacion tipoOrdenacion = TipoOrdenacion.valueOf(cbxTip.getSelectedItem().toString());
        switch (cbxMet.getSelectedIndex()) {
            case 0:
                System.out.println("QuickSort : " + atributo + ", " + tipoOrdenacion.toString());
                cc.getListaPersona().setLista(cc.getListaPersona().getLista().quicksort(atributo, tipoOrdenacion, 0, cc.getListaPersona().getLista().getSize() - 1));
                System.out.println("Ordenanda");
                break;
            case 1:
                System.out.println("Shell : " + atributo + ", " + tipoOrdenacion.toString());
                cc.getListaPersona().setLista(cc.getListaPersona().getLista().ordenar_Shell(atributo, tipoOrdenacion));
                System.out.println("Ordenada");
                break;
        }
        cargarTabla(cc.getListaPersona());
    }

